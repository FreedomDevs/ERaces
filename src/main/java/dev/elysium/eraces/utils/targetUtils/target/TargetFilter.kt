package dev.elysium.eraces.utils.targetUtils.target

/**
 * Перечисление фильтров для таргетинга.
 * Определяет, какие объекты учитывать при raycast или других методах Target API.
 */
enum class TargetFilter {
    /** Учитывать живые сущности (LivingEntity) при поиске */
    ENTITIES,

    /** Берет только первую найденную сущность по линии взгляда */
    FIRST_ENTITY,

    /** Учитывать блоки (например, стены, препятствия) при трассировке */
    BLOCKS,

    /** Прекращать поиск при первом встреченном блоке */
    STOP_AT_BLOCK
}